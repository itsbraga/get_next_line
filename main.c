#include "get_next_line.h"

int	main(void)
{
	char	*line;
	int		fd;

	fd = open("anna.txt", O_RDONLY);
	while (1)
	{
		line = get_next_line(fd);
		if (!line)
			break ;
		printf("%s", line);
		free(line);
	}
	return (0);
}
